package com.kodilla.basic_assertion;

public class Calculator {

    public int sumNumbers(int a, int b) {
        int sum = a + b;
        return sum;
    }

    public int subtractNumbers(int a, int b) {
        int subtract = a - b;
        return subtract;
    }

    public int square (int a) {
        int square = a * a;
        return square;
    }
}
